const cuerpoPag = document.querySelector("body");

const COLOR_TEXTO = "black";
const FUENTE_TEXTO = "arial";
const TAM_TEXTO = "35px";
const COLOR_FONDO = "white";


function modificarPag() {

cuerpoPag.style.color = COLOR_TEXTO;
cuerpoPag.style.backgroundColor = COLOR_FONDO;
cuerpoPag.style.fontFamily = FUENTE_TEXTO;
cuerpoPag.style.fontSize = TAM_TEXTO;
    
}